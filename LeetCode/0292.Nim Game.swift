/*
이 게임에서 **지는 상태(필패 상태)**는 n이 4의 배수일 때입니다.

n % 4 == 0 → 어떤 수(1~3)를 빼도 상대에게 4의 배수 - (1~3) 즉 1,2,3 mod 4 상태를 넘겨주게 되고, 상대는 항상 다시 4의 배수로 맞춰서 가져가며 마지막을 가져갑니다.

n % 4 != 0 → 내가 처음에 n % 4개를 가져가서 남은 돌을 4의 배수로 만들면, 이후 상대가 1~3개 가져갈 때마다 내가 4 - (상대가 가져간 수)로 맞춰서 항상 4개씩 한 턴 사이클로 소진시키며 이길 수 있습니다.
*/

class Solution {
    func canWinNim(_ n: Int) -> Bool {
        n % 4 != 0
    }
}
